#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t		i;
	size_t		cnt;

	if (!dst || !src)
		return (0);
	i = 0;
	cnt = 0;
	while (src[cnt] != '\0')
		cnt++;
	if (size != 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (cnt);
}
